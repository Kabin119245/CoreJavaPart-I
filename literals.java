public class literals {
    public static void main(String[] args) {
        int num1 = 0b101; //binary
        int num2 = 0x1011; //hex
        double num3 = 12e10;
        char c = 'a';
        c++;
        System.out.println(num1);
        System.out.println(num2);
        System.out.println(num3);
        System.out.println(c);

    }
}
