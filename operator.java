public class operator {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 5;
        int res1 = num1 - num2;
        int res2 = num1 * num2;
        int res3 = num1 / num2;
        int res4 = num1 % num2;
        System.out.println(res1 + " " + res2 + " " + res3 + " " + res4);
        int n = 5;
        n += 5; // n = n + 5;
        //n++ post increment
        // ++n; pre increment
        //n-- or --n
        System.out.println(n);
        
    }
}
