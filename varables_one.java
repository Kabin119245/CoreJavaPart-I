public class varables_one {
    public static void main(String[] args) {
        int num1 = 9;
        byte by = 78; 
        short sh = 559;
        long l = 99999999l;
        float f = 99.99f; 
        boolean b = true;
        char c = 'A';
        System.out.println("num1: " + num1 + ", by: " + by + ", sh: " + sh + ", l: " + l + ", f: " + f + ", b: " + b + ", c: " + c);
    }
}
